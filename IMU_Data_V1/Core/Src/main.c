/* USER CODE BEGIN 4 */
int micros(void)
{
  uint32_t m0 = HAL_GetTick();
  uint32_t u0 = SysTick->LOAD - SysTick->VAL;
  uint32_t m1 = HAL_GetTick();
  uint32_t u1 = SysTick->LOAD - SysTick->VAL;

  if (m1 > m0) {
    return (int)( m1 * 1000 + (u1 * 1000) / SysTick->LOAD);
  } else {
    return (int)( m0 * 1000 + (u0 * 1000) / SysTick->LOAD);
  }
}
